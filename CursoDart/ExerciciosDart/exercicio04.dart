// Exercícios de Laços de repetição

// Mostre os números de 1 a 10 usando for.
void main() {
  for (int i = 1; i < 11; i++) {
    print(i);
  }

  // Mostre apenas os números pares de 1 a 20
  for (int i = 1; i < 11; i++) {
    print(i * 2);
  }

  //Use while para contar de 10 até 1.
  int i = 10;
  while (i >= 1) {
    print(i);
    i--;
  }
}
